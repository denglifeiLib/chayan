动作一：两掌握空拳成“熊掌”，拳眼相对，垂于下腹部；目视两拳。<br><br>

动作二：以腰、腹为轴，上体做顺时针摇晃；同时，两拳随之沿右肋部、上腹部、左肋部、下腹部划圆；目随上体摇晃环视。<br><br>
                        
动作三、四：同动作一、二。<br><br>

动作五至八：同动作一至四，惟左右相反。<br><br>

做完最后一动作，两拳变掌下落，自然垂于体侧；目视前方。<br><br>

2.熊晃<br>
 动作一：身体重心右移；左髋上提，牵动左脚离地，再微屈<br>
左膝；两掌握空拳成“熊掌”，目视左前方。<br><br>
                        
动作二：身体重心前移；左脚向左前方落地，全脚掌踏实，脚尖朝前，右腿伸直；身体右转，左臂内旋前靠，左拳摆至左膝前上方，拳心朝左；右拳摆至体后，拳心朝后；目视左
前方。<br><br>

动作三：身体左转，重心后坐；右腿屈膝，左腿伸直；拧腰晃肩，带动两臂前后弧形摆动；右拳摆至左膝前上方，拳心朝右；左拳摆至体后，拳心朝后；目视左前方。
          <br><br>              
动作四：身体右旋，重心前移；左腿屈膝，右腿伸直；同时，左臂内旋前靠，左拳摆至左膝前上方，拳心朝左；右拳摆至体后，拳心朝后；目视左前方。
<br><br>
动作五至八：同动作一至四，惟左右相反。# 重复一至八动作1遍后，左脚上步，开步站立；同时，两手自然垂于体侧；两掌向身体侧前方举起，与胸同高，掌心向上；目视前方。曲肘，两掌内合下按，自然垂于体侧，目视前方。
                        
