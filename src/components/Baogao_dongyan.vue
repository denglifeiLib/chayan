动作三：微屈膝下蹲；同时，两掌向外展开至两掌心间距与掌心至肚脐距离相等，两臂成圆形，并口吐“呼”字音；目视前下方。<br><br>

动作四：两膝缓缓伸直；同时，两掌缓缓向肚脐方向合拢。<br><br>

重复三至四动作5遍，本式共吐“呼”字音6次。
                        
